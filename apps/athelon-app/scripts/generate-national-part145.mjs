// Generate nationalPart145.ts from the FAA AVInfo repair station CSV.
// Usage: node scripts/generate-national-part145.mjs
// Source: knowledge/research/crm-colorado-part145/round-1/team-a-registry/tmp/faa_facility_download.csv

import XLSX from "xlsx";
const { readFile, utils } = XLSX;
import { writeFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const CSV_PATH = resolve(
  __dirname,
  "../../../knowledge/research/crm-colorado-part145/round-1/team-a-registry/tmp/faa_facility_download.csv"
);
const OUTPUT_PATH = resolve(__dirname, "../src/shared/data/nationalPart145.ts");

console.log("Reading FAA CSV...");
const wb = readFile(CSV_PATH, { type: "file" });
const ws = wb.Sheets[wb.SheetNames[0]];
const rows = utils.sheet_to_json(ws);
console.log(`Parsed ${rows.length} total records from FAA CSV.`);

// Filter to US-only records. Note: CSV header has trailing space on "Country "
const usRows = rows.filter((r) => {
  const country = (r["Country"] || r["Country "] || "").toString().trim();
  return country === "UNITED STATES";
});
console.log(`Filtered to ${usRows.length} UNITED STATES records.`);

// Deduplicate on Cert_No (first-occurrence wins)
const seen = new Set();
const deduped = [];
const dupes = [];
for (const r of usRows) {
  const certNo = (r["Cert_No"] || "").toString().trim();
  if (!certNo) continue;
  if (seen.has(certNo)) {
    dupes.push(certNo);
    continue;
  }
  seen.add(certNo);
  deduped.push(r);
}
if (dupes.length > 0) {
  console.log(`Removed ${dupes.length} duplicate Cert_No(s): ${dupes.join(", ")}`);
}

function clean(val) {
  if (val == null) return null;
  const s = val.toString().trim();
  return s === "" ? null : s;
}

function cleanPhone(val) {
  const s = clean(val);
  if (!s) return null;
  // Strip non-digit chars, keep if 7+ digits
  const digits = s.replace(/\D/g, "");
  return digits.length >= 7 ? s : null;
}

function deriveContactCompleteness(hasPhone, hasEmail) {
  if (hasPhone && hasEmail) return "full";
  if (hasPhone || hasEmail) return "partial";
  return "none";
}

// Map to lean schema
const records = deduped.map((r) => {
  const certNo = clean(r["Cert_No"]);
  const phone = cleanPhone(r["Agency Phone Number"]) || cleanPhone(r["Liaison Phone Number"]);
  const email = clean(r["Agency Email"]) || clean(r["Email"]) || clean(r["Liaison Email"]);
  const hasPhone = phone !== null;
  const hasEmail = email !== null;

  // Concatenate address lines
  const addr1 = clean(r["Address Line 1"]);
  const addr2 = clean(r["Address Line 2"]);
  const addr3 = clean(r["Address Line 3"]);
  const street = [addr1, addr2, addr3].filter(Boolean).join(", ") || null;

  return {
    entityId: certNo,
    legalName: clean(r["Agency Name"]) || "UNKNOWN",
    dbaName: clean(r["DBA"]),
    street,
    city: clean(r["City"]) || "UNKNOWN",
    state: clean(r["State/Province"]) || "UNKNOWN",
    zip: clean(r["Postal Code"]),
    phone,
    email,
    certNo,
    dsgnCode: clean(r["DSGN_CODE"]),
    hasPhone,
    hasEmail,
    contactCompleteness: deriveContactCompleteness(hasPhone, hasEmail),
    dataSource: "faa-csv",
    faaUpdateDate: clean(r["Update_Date"]),
  };
});

// Sort by state, then legalName (deterministic output)
records.sort((a, b) => {
  const stateCompare = a.state.localeCompare(b.state);
  if (stateCompare !== 0) return stateCompare;
  return a.legalName.localeCompare(b.legalName);
});

// Compute state breakdown
const stateBreakdown = {};
for (const r of records) {
  stateBreakdown[r.state] = (stateBreakdown[r.state] || 0) + 1;
}

// Compute contact stats
const contactStats = { full: 0, partial: 0, none: 0 };
for (const r of records) {
  contactStats[r.contactCompleteness]++;
}

console.log(`\nGenerated ${records.length} unique US Part 145 records.`);
console.log(`Contact completeness: full=${contactStats.full}, partial=${contactStats.partial}, none=${contactStats.none}`);
console.log(`States: ${Object.keys(stateBreakdown).length}`);
console.log("Top 10 states:", Object.entries(stateBreakdown).sort((a, b) => b[1] - a[1]).slice(0, 10).map(([s, c]) => `${s}(${c})`).join(", "));

// Emit TypeScript
function escapeString(s) {
  if (s === null) return "null";
  return JSON.stringify(s);
}

const lines = [
  `// Auto-generated by scripts/generate-national-part145.mjs — DO NOT EDIT`,
  `// Source: knowledge/research/crm-colorado-part145/round-1/team-a-registry/tmp/faa_facility_download.csv`,
  `// Records: ${records.length} US Part 145 repair stations`,
  `// Generated: ${new Date().toISOString().split("T")[0]}`,
  ``,
  `export type NationalProspectRecord = {`,
  `  entityId: string;`,
  `  legalName: string;`,
  `  dbaName: string | null;`,
  `  street: string | null;`,
  `  city: string;`,
  `  state: string;`,
  `  zip: string | null;`,
  `  phone: string | null;`,
  `  email: string | null;`,
  `  certNo: string;`,
  `  dsgnCode: string | null;`,
  `  hasPhone: boolean;`,
  `  hasEmail: boolean;`,
  `  contactCompleteness: "full" | "partial" | "none";`,
  `  dataSource: "faa-csv";`,
  `  faaUpdateDate: string | null;`,
  `};`,
  ``,
  `export const nationalPart145Records: NationalProspectRecord[] = [`,
];

for (const r of records) {
  lines.push(`  {`);
  lines.push(`    entityId: ${escapeString(r.entityId)},`);
  lines.push(`    legalName: ${escapeString(r.legalName)},`);
  lines.push(`    dbaName: ${escapeString(r.dbaName)},`);
  lines.push(`    street: ${escapeString(r.street)},`);
  lines.push(`    city: ${escapeString(r.city)},`);
  lines.push(`    state: ${escapeString(r.state)},`);
  lines.push(`    zip: ${escapeString(r.zip)},`);
  lines.push(`    phone: ${escapeString(r.phone)},`);
  lines.push(`    email: ${escapeString(r.email)},`);
  lines.push(`    certNo: ${escapeString(r.certNo)},`);
  lines.push(`    dsgnCode: ${escapeString(r.dsgnCode)},`);
  lines.push(`    hasPhone: ${r.hasPhone},`);
  lines.push(`    hasEmail: ${r.hasEmail},`);
  lines.push(`    contactCompleteness: ${escapeString(r.contactCompleteness)},`);
  lines.push(`    dataSource: "faa-csv" as const,`);
  lines.push(`    faaUpdateDate: ${escapeString(r.faaUpdateDate)},`);
  lines.push(`  },`);
}

lines.push(`];`);
lines.push(``);
lines.push(`export const nationalPart145Pack = {`);
lines.push(`  totalRecords: ${records.length},`);
lines.push(`  stateCount: ${Object.keys(stateBreakdown).length},`);
lines.push(`  contactStats: ${JSON.stringify(contactStats)},`);
lines.push(`  topStates: ${JSON.stringify(Object.entries(stateBreakdown).sort((a, b) => b[1] - a[1]).slice(0, 10).map(([state, count]) => ({ state, count })))},`);
lines.push(`};`);
lines.push(``);

writeFileSync(OUTPUT_PATH, lines.join("\n"), "utf-8");
console.log(`\nWrote ${OUTPUT_PATH}`);
