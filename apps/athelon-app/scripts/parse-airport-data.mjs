// Parse FAA NFDC airport data XLSX into static TypeScript files.
// Usage: node scripts/parse-airport-data.mjs
// Source: knowledge/research/airport data/all-airport-data.xlsx

import XLSX from "xlsx";
const { readFile, utils } = XLSX;
import { writeFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const XLSX_PATH = resolve(
  __dirname,
  "../../../knowledge/research/airport data/all-airport-data.xlsx"
);
const OUTPUT_DIR = resolve(__dirname, "../src/shared/data");

console.log("Reading XLSX...");
const wb = readFile(XLSX_PATH);
const ws = wb.Sheets["Airports"];
const rows = utils.sheet_to_json(ws);
console.log(`Parsed ${rows.length} facility records from Airports sheet.`);

// Column mapping: XLSX header → output field
const COLUMN_MAP = {
  "Loc Id": "faaLocId",
  "ICAO Id": "icaoId",
  Name: "facilityName",
  "Facility Type": "facilityType",
  "State Name": "state",
  City: "city",
  County: "county",
  "ARP Latitude DD": "latDecimal",
  "ARP Longitude DD": "lonDecimal",
  Ownership: "ownership",
  "Airframe Repair": "airframeRepair",
  "Power Plant Repair": "powerPlantRepair",
  "Fuel Types": "fuelTypes",
  "Certification Type Date": "part139Cert",
  "NPIAS Hub": "npiasHubClass",
};

// Aircraft count columns to sum for basedAircraftTotal
const AIRCRAFT_COLS = [
  "Single Engine Aircraft",
  "Multi Engine Aircraft",
  "Jet Engine Aircraft",
  "Helicopters",
  "Gliders Operational",
  "Ultralights",
];

// Ops columns to sum for annualOpsTotal
const OPS_COLS = [
  "Commercial Operations",
  "Commuter Operations",
  "Air Taxi Operations",
  "GA Local Operations",
  "GA Itin Operations",
  "Military Operations",
];

function parseNum(val) {
  if (val === undefined || val === null || val === "") return null;
  const n = Number(val);
  return isNaN(n) ? null : n;
}

function parseStr(val) {
  if (val === undefined || val === null || val === "") return null;
  return String(val).trim();
}

function sumCols(row, cols) {
  let total = 0;
  let hasAny = false;
  for (const col of cols) {
    const n = parseNum(row[col]);
    if (n !== null) {
      total += n;
      hasAny = true;
    }
  }
  return hasAny ? total : null;
}

function normalizeRepair(val) {
  const s = parseStr(val);
  if (!s) return null;
  const upper = s.toUpperCase();
  if (upper === "MAJOR" || upper === "MINOR" || upper === "NONE") return upper;
  return s;
}

// Extract Part 139 cert type from "Certification Type Date" field.
// Format examples: "I E S 02/1995", "IV 03/2020". Extract letter portion before date.
function parsePart139(val) {
  const s = parseStr(val);
  if (!s) return null;
  // Grab everything before the MM/YYYY date, trim it
  const match = s.match(/^(.+?)\s+\d{2}\/\d{4}/);
  if (match) return match[1].trim();
  return s;
}

const records = [];

for (const row of rows) {
  const faaLocId = parseStr(row["Loc Id"]);
  if (!faaLocId) continue; // skip rows without a location ID

  const latDecimal = parseNum(row["ARP Latitude DD"]);
  const lonDecimal = parseNum(row["ARP Longitude DD"]);

  records.push({
    faaLocId,
    icaoId: parseStr(row["ICAO Id"]),
    facilityName: parseStr(row["Name"]) || "",
    facilityType: parseStr(row["Facility Type"]) || "AIRPORT",
    state: parseStr(row["State Name"]) || "",
    city: parseStr(row["City"]) || "",
    county: parseStr(row["County"]),
    latDecimal: latDecimal ?? 0,
    lonDecimal: lonDecimal ?? 0,
    ownership: parseStr(row["Ownership"]) || "PR",
    airframeRepair: normalizeRepair(row["Airframe Repair"]),
    powerPlantRepair: normalizeRepair(row["Power Plant Repair"]),
    fuelTypes: parseStr(row["Fuel Types"]),
    part139Cert: parsePart139(row["Certification Type Date"]),
    npiasHubClass: parseStr(row["NPIAS Hub"]),
    basedAircraftTotal: sumCols(row, AIRCRAFT_COLS),
    annualOpsTotal: sumCols(row, OPS_COLS),
  });
}

console.log(`Extracted ${records.length} records.`);

// Count stats
const withIcao = records.filter((r) => r.icaoId).length;
const byType = {};
for (const r of records) {
  byType[r.facilityType] = (byType[r.facilityType] || 0) + 1;
}
console.log(`ICAO codes: ${withIcao}`);
console.log("By type:", byType);

// Write faaAirports.ts
const dataFile = `// Auto-generated by scripts/parse-airport-data.mjs — DO NOT EDIT
// Source: FAA NFDC all-airport-data.xlsx, effective 2026-02-19
// Records: ${records.length} facilities
// Regenerate: pnpm data:parse-airports

import type { FaaAirportRecord } from "./faaAirportTypes";

// eslint-disable-next-line @typescript-eslint/no-explicit-any
export const faaAirports = ${JSON.stringify(records, null, 0)} as any as FaaAirportRecord[];
`;

writeFileSync(resolve(OUTPUT_DIR, "faaAirports.ts"), dataFile, "utf-8");
console.log(`Wrote faaAirports.ts (${(dataFile.length / 1024 / 1024).toFixed(1)}MB)`);

// Write faaAirportIndex.ts
const indexFile = `// Auto-generated by scripts/parse-airport-data.mjs — DO NOT EDIT
// Provides O(1) lookups by ICAO code and FAA Location Identifier.

import type { FaaAirportRecord } from "./faaAirportTypes";

let _airports: FaaAirportRecord[] | null = null;
let _byIcao: Map<string, FaaAirportRecord> | null = null;
let _byFaaLocId: Map<string, FaaAirportRecord> | null = null;

async function ensureLoaded() {
  if (_airports) return;
  const { faaAirports } = await import("./faaAirports");
  _airports = faaAirports;

  _byIcao = new Map();
  _byFaaLocId = new Map();

  for (const record of _airports) {
    _byFaaLocId.set(record.faaLocId, record);
    if (record.icaoId) {
      _byIcao.set(record.icaoId, record);
    }
  }
}

/** Lazy-load airport data and return all records */
export async function getAllAirports(): Promise<FaaAirportRecord[]> {
  await ensureLoaded();
  return _airports!;
}

/** Lookup by ICAO code (e.g. "KDEN"). Returns undefined if not found or no ICAO. */
export async function getAirportByIcao(icao: string): Promise<FaaAirportRecord | undefined> {
  await ensureLoaded();
  return _byIcao!.get(icao.toUpperCase());
}

/** Lookup by FAA Location Identifier (e.g. "DEN"). Returns undefined if not found. */
export async function getAirportByFaaLocId(locId: string): Promise<FaaAirportRecord | undefined> {
  await ensureLoaded();
  return _byFaaLocId!.get(locId.toUpperCase());
}

/** Lookup by either ICAO or FAA Loc Id. Tries ICAO first (4-char), then FAA Loc Id. */
export async function getAirport(code: string): Promise<FaaAirportRecord | undefined> {
  await ensureLoaded();
  const upper = code.toUpperCase();
  return _byIcao!.get(upper) ?? _byFaaLocId!.get(upper);
}

/**
 * Search airports by query string. Matches against ICAO, FAA Loc Id,
 * facility name, and city. Returns up to \`limit\` results.
 */
export async function searchAirports(
  query: string,
  options?: { limit?: number; facilityType?: string }
): Promise<FaaAirportRecord[]> {
  await ensureLoaded();
  const q = query.toUpperCase();
  const limit = options?.limit ?? 20;
  const typeFilter = options?.facilityType?.toUpperCase();
  const results: FaaAirportRecord[] = [];

  for (const record of _airports!) {
    if (typeFilter && record.facilityType.toUpperCase() !== typeFilter) continue;

    if (
      record.faaLocId.toUpperCase().includes(q) ||
      (record.icaoId && record.icaoId.toUpperCase().includes(q)) ||
      record.facilityName.toUpperCase().includes(q) ||
      record.city.toUpperCase().includes(q)
    ) {
      results.push(record);
      if (results.length >= limit) break;
    }
  }

  // Sort: exact ID matches first, then alphabetical
  results.sort((a, b) => {
    const aExact = a.faaLocId.toUpperCase() === q || a.icaoId?.toUpperCase() === q ? 0 : 1;
    const bExact = b.faaLocId.toUpperCase() === q || b.icaoId?.toUpperCase() === q ? 0 : 1;
    if (aExact !== bExact) return aExact - bExact;
    return a.facilityName.localeCompare(b.facilityName);
  });

  return results;
}
`;

writeFileSync(resolve(OUTPUT_DIR, "faaAirportIndex.ts"), indexFile, "utf-8");
console.log("Wrote faaAirportIndex.ts");

console.log("Done.");
